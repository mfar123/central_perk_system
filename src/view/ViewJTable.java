    private void button_cadastratActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_cadastratActionPerformed
        if (txtDesc.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insira a Descrição");
            txtDesc.requestFocusInWindow();
            return;
        }
        if (textQtd.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insira a Quantidade");
            textQtd.requestFocusInWindow();
            return;
        }
        if (textPreco.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insira o Preço");
            textPreco.requestFocusInWindow();
            return;
        }

        /*DefaultTableModel dtmProdutos= (DefaultTableModel) jTProdutos.getModel();
    Object[] dados = {txtDesc.getText(),textQtd.getText(),textPreco.getText()};
    dtmProdutos.addRow(dados);* ------------> sem banco de dados*/
        Produtos p = new Produtos();
        ProdutoDA0 dao = new ProdutoDA0();

        p.setDescricao(txtDesc.getText());
        p.setQtd(Integer.parseInt(textQtd.getText()));
        p.setPreço(Double.parseDouble(textPreco.getText()));

        dao.create(p);

        readJTable();

        txtDesc.setText("");
        textQtd.setText("");
        textPreco.setText("");
        txtDesc.requestFocusInWindow();


    }//GEN-LAST:event_button_cadastratActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jTProdutos.getSelectedRow() != -1) {

            Produtos p = new Produtos();
            ProdutoDA0 dao = new ProdutoDA0();

            p.setDescricao(txtDesc.getText());
            p.setQtd(Integer.parseInt(textQtd.getText()));
            p.setPreço(Double.parseDouble(textPreco.getText()));
            p.setId((int) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0));
            dao.update(p);

            readJTable();

            txtDesc.setText("");
            textQtd.setText("");
            textPreco.setText("");
            txtDesc.requestFocusInWindow();

        } else {

            JOptionPane.showMessageDialog(null, "Selecione um produto para atualizar");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

